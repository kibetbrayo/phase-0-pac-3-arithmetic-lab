// Math functions
function add(a, b) {
  return a + b;
}

function subtract(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

// Increment and decrement functions
function increment(n) {
  return n + 1;
}

function decrement(n) {
  return n - 1;
}

// makeInt function
function makeInt(n) {
  return parseInt(n, 10);
}

// preserveDecimal function
function preserveDecimal(n) {
  return parseFloat(n);
}
